#!/bin/bash

# ANSI color codes
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BROWN='\033[0;33m'
RESET='\033[0m'

# ASCII art with color codes
echo -e "                                   "
echo -e "           ${GREEN}__${RESET}       "
echo -e "           ${GREEN} ><<         ${YELLOW}_       "
echo -e "           ${GREEN}>>><<<${RESET}      ${YELLOW}(_)       "
echo -e "            ${GREEN}>%%<<<       "
echo -e "           ${GREEN}>>%%\\%<<${RESET}       "
echo -e "            ${GREEN}>>%|%<<<${RESET}       "
echo -e "          ${GREEN}>>>%%|%<<<${RESET}       "
echo -e "            ${GREEN}>>%|%%<<${RESET}       "
echo -e "          ${GREEN}>\` >%|%%<<<${RESET}       "
echo -e "             ${GREEN}>>|%%<<<${RESET}       "
echo -e "              ${GREEN}>|<<-<<<<       "
echo -e "               ${BROWN}||  ${GREEN}\`<<       "
echo -e "             ${GREEN}>${BROWN}-||${GREEN}<       "
echo -e "               ${BROWN}||-       "
echo -e "               ${BROWN}||--${GREEN}<<       "
echo -e "            ${GREEN}>${BROWN}--||       "
echo -e "               ${BROWN}||${RESET}       "
echo -e "               ${BROWN}||-${RESET}       "
echo -e "               ${BROWN}||${RESET}       "
echo -e "               ${BROWN}||${RESET}       "
echo -e "               ${BROWN}||-${RESET}       "
echo -e "         ${GREEN}\\/    ${BROWN}||${RESET}       "
echo -e "         ${GREEN}\\/${RESET}    ${BROWN}||-${RESET}  "
echo -e "         ${GREEN}\\//  %||%     "
echo -e "       ${GREEN}V|\\// %%||%%  .   .       "
echo -e "       ${GREEN}V|\\// %%||%%  ;:,:;       "
echo -e "       ${GREEN}V|\\///%%||%% ;:;:;:       "
echo -e "       ${GREEN}\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"\"${RESET}  "
